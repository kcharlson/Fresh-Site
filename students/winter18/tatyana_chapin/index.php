    <h2>Fruits I like, And Stuff About Them</h2>
    <p>People in my life would consider me a picky eater. I’ve been asked questions like “What do you eat?” and “Do you like anything?”. This booklet was made as an answer to those questions and an informative guide. While I do like more than four fruits (and food other than fruit) at least this proves I like something. </p>
    
