    <h2>You’re A Bad/Good Customer </h2>
    <p>I’ve been working in customer service for a while now and it isn’t great. It takes a lot of strength to not yell at someone who is yelling at you. To help channel my frustrations, I made a bad customer form and for those rare occasions that a customer isn’t all bad, a good customer form. Intended to be given to the customer but not recommended. </p>
    
