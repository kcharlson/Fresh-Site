    <h2>Oregon Entry Wall</h2>
    <p>The Alumni Association at the University of Oregon wanted to create a space that both recognized the alumni who have attended the University while also inspiring the future. The alumni wall is a digital wood wall installation with LED displays, screens showing campus events and energy consumption and laser etched names of the alumni who helped make the alumni center possible. While at Second Story, I designed the typographic system for the laser etched alumni names creating a hierarchy for the names based on donation.</p>

    <img class="artwork" src="students/winter18/lisa_kennedy/l_kennedy_image2.png" alt="Photo of Project 2">
    <h2>Sesame Street</h2>
    <p>Seasame Workshop was looking to create an interactive table in the lobby of their New York City headquarters to communicate their mission of helping children via educational media.<br><br>
    Inspired by their mission, I used the nickname the “Longest Street in the World” to design and illustrate an unorthodox and interactive map intended to be viewed from each side of the Microsoft Surface multitouch table showcasing the global impact of the work of Sesame Workshop.</p>

    <img class="artwork" src="students/winter18/lisa_kennedy/l_kennedy_image3.png" alt="Photo of Project 3">
    <h2>Descent With Modification</h2>
    <p>My design thesis, "On Descent With Modification," hypothesizes that ideas expressed in Charles Darwin's "On the Origin of Species," evolved from his grandfather Erasmus Darwin's poem "The Temple of Nature" via: (1) traits Charles inherited from his grandfather and (2) through these traits being expressed during Charles’ interaction with his environment.</p>

    <img class="artwork" src="students/winter18/lisa_kennedy/l_kennedy_image4.png" alt="Photo of Project 4">
    <h2>Discover Diplomacy</h2>
    <p>The United States Diplomacy Center commissioned Second Story to create a website educating people about diplomacy and the work of the Department of State. <a href="https://diplomacy.state.gov/discoverdiplomacy/" style="color:#f89a2e;text-decoration:underline;">Discover Diplomacy</a> highlights what diplomacy is and the practice of diplomacy around the world.<br><br>
    Using the globe and passports as the metaphor, I designed a dynamic HTML5 interactive map for visitors to explore and learn about the global outreach of the Department of State and people, places and issues they address. To support the content, I wrote, designed and illustrated an animated video describing what diplomacy is.</p>

