    <h2>Pip's Stamp Design</h2>
    <p>This stamp illustration was picked in a local design competition hosted by/for Pip's Doughnuts in Portland, OR. The brand is very up-beat, focused on relationships with customers, and they provide a cozy atmosphere for the close-knit community within their shop. They decorate their storefront with screen-printed flags, so I thought a cheery message on a flag shaped design would compliment their brand aesthetic, and over-all good vibes.</p>
    
