    <h2>SXSW Booklet</h2>
    <p>SXSW is a dynamic conference and festival that celebrates the convergence of the interactive, film, and music industries. For my booklet, I wanted to keep the layout fun, but organized. I wanted the booklet to appeal to creative/innovative thinkers, which is translated through the icon designs used in the booklet. The tabs, contrastic colors, and illustrations encourages guests to have fun and think outside the box.</p>
    
