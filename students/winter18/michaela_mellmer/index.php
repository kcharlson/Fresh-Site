    <h2>LASR Logo</h2>
    <p>Lute Air Student Radio is a student-run, internet-based radio station operating out of Pacific Lutheran University. They seek to provide a wide array of music and talk shows to students at PLU, the community, and the world. I wanted to create a simple logo, resembling a the classic vinyl record player, while taking on that form with a modern and minimalistic twist. This logo is versatile, and simple enough to be placed on multiple promo pieces. From this logo, buttons, tee shirts, and stickers were created.</p>
    
