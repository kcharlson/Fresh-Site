    <h2>Anthropology Magazine</h2>
    <p>Anthropology's beautiful pages are an inspiration to a viewers everyday reflection on life; the articles specifically focus on the notion of “less is more” when discussing interior design. My magazine design presents a modern and clean aesthetic, with crisp images and a high contrast earthy color palette that ties back to the minimalist theme. The classic serif typeface and use of white space creates for a luxurious and unwinding reading experience.</p>
    
