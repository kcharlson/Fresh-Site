    <h2>The Princess Bride Movie Poster</h2>
    <p>This project was a movie poster for one of my favorite movies, The princess Bride. The most memorable aspects of the movie for me were the sword fighting and the love story. I portrayed these things in the poster by incorporating a sword into the heart using negative space. The hand lettering is very loose and fun, such as the witty dialogue in the movie.</p>
    
    <img class="artwork" src="students/winter18/denise_hernandez/d_hernandez_image2.jpg" alt="Photo of Project 2">
    <h2>Chetos Enchilosos</h2>
    <p>Chetos enchilosos is a series of illustrations of flammin hot cheetos. Hot cheetos remind me of my childhood. I remember covering them with chile & limon and sharing them with my friends. I wanted to create fun & bold illustrations of my favorite snacks to show my appreciation for them.</p>
    
    <img class="artwork" src="students/winter18/denise_hernandez/d_hernandez_image3.jpg" alt="Photo of Project 3">
    <h2>Border Angels</h2>
    <p>Branding project for the non-profit Organization, Border Angels located in San Diego, CA. Their slogan is "Saving lives, making a difference" which they do by preventing unnecessary deaths through desert water drops, border rescue stations  and day labourer outreach. My main goal with this branding was to evoke hope and show that despite borders, we can still work together to help humanity.</p>
    
    
    <img class="artwork" src="students/winter18/denise_hernandez/d_hernandez_image4.jpg" alt="Photo of Project 1">
    <h2>Yo Soy Chicana</h2>
    <p>Yo Soy Chicana translates into "I am Mexican-American". It's an event that celebrates the voices of Chicana Women and Chicana feminism. I created the logo, branding, and made a poster series to promote the event. I made collages of inspirational Chicana women such as Gloria Anzaldua and Sandra Cisneros. The event is fun, bold, and all about community which is why I chose to incorporate bright colors & hand lettering.</p>
    
